    // Configura o tema inicial da tela
    private void configurarTema() {
        boolean isDark = UIManager.getLookAndFeel() instanceof FlatDarkLaf;
        jRTema.setSelected(isDark);
        if (isDark) {
            jRTema.setIcon(new FlatSVGIcon("imgs/dark-mode.svg", 20, 20));
            jRTema.setText("Tema Escuro");
        } else {
            jRTema.setIcon(new FlatSVGIcon("imgs/light-mode.svg", 20, 20));
            jRTema.setText("Tema Claro");
        }

    }

