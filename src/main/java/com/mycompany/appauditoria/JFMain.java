    private void jRTemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRTemaActionPerformed
        if (jRTema.isSelected()) {
            jRTema.setText("Tema Escuro");
            jRTema.setIcon(new FlatSVGIcon("imgs/dark-mode.svg", 20, 20));
            FlatDarkLaf.setup();
            FlatSVGIcon.ColorFilter.getInstance().add(Color.BLACK, Color.WHITE);
        } else {
            jRTema.setText("Tema Claro");
            jRTema.setIcon(new FlatSVGIcon("imgs/light-mode.svg", 20, 20));
            FlatLightLaf.setup();
            FlatSVGIcon.ColorFilter.getInstance().add(Color.BLACK, Color.BLACK);
        }

        FlatLaf.updateUI();
    }//GEN-LAST:event_jRTemaActionPerformed

